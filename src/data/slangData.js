export function searchEntries(query) {
  const q = query.toLowerCase();
  return slangData.filter(entry =>
    entry.word.toLowerCase().includes(q) ||
    entry.meaning.toLowerCase().includes(q) ||
    entry.tags.some(t => t.includes(q))
  );
}

export function getRandomEntry() {
  return slangData[Math.floor(Math.random() * slangData.length)];
}

export function filterByCategory(category) {
  if (category === "All") return slangData;
  const map = {
    "Gaming": "Gaming",
    "TikTok": "TikTok",
    "Reddit": "Reddit",
    "Meme Terms": "Meme"
  };
  return slangData.filter(e =>
    e.category === map[category] ||
    e.tags.includes(map[category]?.toLowerCase())
  );
}
